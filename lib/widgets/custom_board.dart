class CustomBoardState extends State<CustomBoard> {
  late List<List<int>> selectedBoard;
  late List<List<bool>> tileColors;
  List<List<int>> winningLines = [];
  bool isWinner = false;
  late bool isLocked;

  late ConfettiController _confettiController;

  @override
  void initState() {
    super.initState();
    isLocked = widget.initialLockState;
    _confettiController =
        ConfettiController(duration: const Duration(seconds: 3));
    selectedBoard =
        predefinedBoards[(widget.number - 1) % predefinedBoards.length];
    tileColors = List.generate(
        5,
        (row) => List.generate(5, (col) {
              int number = selectedBoard[row][col];
              return (row == 2 && col == 2)
                  ? true
                  : widget.calledNumberss.contains(number);
            }));
    checkForBingo();
  }

  @override
  void dispose() {
    _confettiController.dispose();
    super.dispose();
  }

  void checkForBingo() {
    List<List<int>> newWinningLines = [];

    // Check rows
    for (int row = 0; row < 5; row++) {
      if (tileColors[row].every((marked) => marked)) {
        newWinningLines.add([for (int i = 0; i < 5; i++) row * 5 + i]);
      }
    }

    // Check columns
    for (int col = 0; col < 5; col++) {
      if ([0, 1, 2, 3, 4].every((row) => tileColors[row][col])) {
        newWinningLines.add([for (int i = 0; i < 5; i++) i * 5 + col]);
      }
    }

    // Check diagonals
    if ([0, 1, 2, 3, 4].every((i) => tileColors[i][i])) {
      newWinningLines.add([0, 6, 12, 18, 24]);
    }

    if ([0, 1, 2, 3, 4].every((i) => tileColors[i][4 - i])) {
      newWinningLines.add([4, 8, 12, 16, 20]);
    }

    // Check four corners
    bool hasFourCorners = tileColors[0][0] &&
        tileColors[0][4] &&
        tileColors[4][0] &&
        tileColors[4][4];
    if (hasFourCorners) {
      newWinningLines.add([0, 4, 20, 24]);
    }

    // Verify last called number is in at least one winning line
    bool containsLastCalledNumber = newWinningLines.any((line) => line.any(
        (index) =>
            selectedBoard[index ~/ 5][index % 5] == widget.lastCalledNumber));

    setState(() {
      winningLines = newWinningLines;
      isWinner = winningLines.length >= widget.requiredLines &&
          containsLastCalledNumber;
      if (isWinner) {
        _confettiController.play();
        if (widget.luckyNumbers.contains(widget.lastCalledNumber)) {
          AudioPlayer().play(AssetSource('audio_recordings/with_bones.mp3'));
          widget.onBonusWin(widget.lastCalledNumber);
        } else {
          AudioPlayer().play(AssetSource('audio_recordings/winning_sound.mp3'));
        }
      }
    });

    widget.onBingoCheck(isWinner);
  }

